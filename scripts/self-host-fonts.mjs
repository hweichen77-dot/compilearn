// Parse the fetched Google Fonts CSS, keep only the `latin` @font-face blocks,
// download those woff2 into website/fonts, and emit website/fonts/fonts.css that
// points at the local files. Lets the site ship with font-src 'self' (no CDN).
import { readFileSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'

const OUT_DIR = join(process.cwd(), 'website', 'fonts')
const sources = [
  { css: '/tmp/f-bri.css', slug: 'bricolage' },
  { css: '/tmp/f-han.css', slug: 'hanken' },
  { css: '/tmp/f-spl.css', slug: 'spline' },
]

let outCss = '/* Self-hosted fonts (latin subset). Generated by scripts/self-host-fonts.mjs */\n'
const downloads = []

for (const { css, slug } of sources) {
  const text = readFileSync(css, 'utf8')
  // Each block: a `/* subset */` comment followed by an @font-face {...}
  const re = /\/\*\s*([\w-]+)\s*\*\/\s*(@font-face\s*\{[^}]*\})/g
  let m
  while ((m = re.exec(text))) {
    const subset = m[1]
    if (subset !== 'latin') continue
    let block = m[2]
    const weightMatch = block.match(/font-weight:\s*([^;]+);/)
    const weight = (weightMatch ? weightMatch[1].trim() : '400').replace(/\s+/g, '')
    const urlMatch = block.match(/url\((https:\/\/[^)]+\.woff2)\)/)
    if (!urlMatch) continue
    const url = urlMatch[1]
    const fname = `${slug}-${weight.replace(/\.\.|\s/g, '_')}.woff2`
    downloads.push({ url, fname })
    block = block.replace(/url\(https:\/\/[^)]+\.woff2\)/, `url(./${fname})`)
    outCss += block + '\n'
  }
}

writeFileSync(join(OUT_DIR, 'fonts.css'), outCss)
writeFileSync('/tmp/font-downloads.txt', downloads.map(d => `${d.url} ${d.fname}`).join('\n'))
console.log(`Latin @font-face blocks: ${downloads.length}. Wrote fonts.css.`)
for (const d of downloads) console.log(`  ${d.fname}`)
